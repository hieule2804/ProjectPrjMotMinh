package model;

/**
 *
 * @author ADMIN
 */
public class orderDetail {
    private int oid ,bid,quantity,price;

    public orderDetail() {
    }

    public orderDetail(int oid, int bid, int quantity, int price) {
        this.oid = oid;
        this.bid = bid;
        this.quantity = quantity;
        this.price = price;
    }

    public int getOid() {
        return oid;
    }

    public void setOid(int oid) {
        this.oid = oid;
    }

    public int getBid() {
        return bid;
    }

    public void setBid(int bid) {
        this.bid = bid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
}
